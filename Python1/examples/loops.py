#!/usr/bin/env python
# encoding=utf-8

# seznam se třemi prvky
seznam = ["a", "b", "c"]

# iterace přes všechny prvky seznamu
for i in seznam:
    print(i)

print()

# n-tice se třemi prvky
ntice = (123, 456, 789)

# iterace přes všechny prvky n-tice
for i in ntice:
    print(i)

print()

# smyčka typu while
i = 1
while i <= 2000:
    print(i)
    i *= 2
